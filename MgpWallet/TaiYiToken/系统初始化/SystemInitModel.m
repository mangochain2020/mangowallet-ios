#import "SystemInitModel.h"

@implementation SystemInitModel
+ (NSDictionary *)modelContainerPropertyGenericClass {
    return @{@"iosversion" : [IosVersion class],
             @"aboutUs" : [AboutUs class]
             };
}
-(void)datacheck{
    if ([self.iosversion isEqual:[NSNull null]]) {
        self.iosversion = [IosVersion new];
        self.iosversion.forceUpdate = -1;
        self.iosversion.intro = @"";
        self.iosversion.length = 0;
        self.iosversion.link = @"";
        self.iosversion.md5 = @"";
        self.iosversion.version = @"";
    }
    if ([self.aboutUs isEqual:[NSNull null]]) {
        self.aboutUs.website = @"";
        self.aboutUs.twitter = @"";
        self.aboutUs.wechat = @"";
        self.aboutUs.telegram = @"";
        self.aboutUs.fb = @"";
        self.aboutUs.weibo = @"";
    }
    if ([self.mlSiteUrl isEqual:[NSNull null]]) {
        self.mlSiteUrl = @"";
    }
    if ([self.misChainBrowser isEqual:[NSNull null]]) {
        self.misChainBrowser = @"";
    }
    if ([self.nodeBtcList isEqual:[NSNull null]]) {
        self.nodeBtcList = [NSMutableArray new];
    }
    if ([self.nodeEosList isEqual:[NSNull null]]) {
        self.nodeEosList = [NSMutableArray new];
    }
    if ([self.nodeEthList isEqual:[NSNull null]]) {
        self.nodeEthList = [NSMutableArray new];
    }
    if ([self.nodeMisList isEqual:[NSNull null]]) {
        self.nodeMisList = [NSMutableArray new];
    }
}
- (void)encodeWithCoder:(NSCoder *)coder
{
    //告诉系统归档的属性是哪些
    unsigned int count = 0;//表示对象的属性个数
    Ivar *ivars = class_copyIvarList([SystemInitModel class], &count);
    for (int i = 0; i<count; i++) {
        //拿到Ivar
        Ivar ivar = ivars[i];
        const char *name = ivar_getName(ivar);//获取到属性的C字符串名称
        NSString *key = [NSString stringWithUTF8String:name];//转成对应的OC名称
        //归档 -- 利用KVC
        [coder encodeObject:[self valueForKey:key] forKey:key];
    }
    free(ivars);//在OC中使用了Copy、Creat、New类型的函数，需要释放指针！！（注：ARC管不了C函数）
}

- (instancetype)initWithCoder:(NSCoder *)coder
{
    self = [super init];
    if (self) {
        //解档
        unsigned int count = 0;
        Ivar *ivars = class_copyIvarList([SystemInitModel class], &count);
        for (int i = 0; i<count; i++) {
            //拿到Ivar
            Ivar ivar = ivars[i];
            const char *name = ivar_getName(ivar);
            NSString *key = [NSString stringWithUTF8String:name];
            //解档
            id value = [coder decodeObjectForKey:key];
            // 利用KVC赋值
            [self setValue:value forKey:key];
        }
        free(ivars);
    }
    return self;
}
@end

